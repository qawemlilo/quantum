<h1>QFP Client File Management System</h1> 

<p>QFP Client File Management System is a Joomla! 1.6 component that allows an administrator to log in via the front-end and create new user accounts.</p>

<p>Once a user has been created, the administrator can upload different types of files for that user.</p>

<p>When the user logs in, uploaded files are available for download.</p>

